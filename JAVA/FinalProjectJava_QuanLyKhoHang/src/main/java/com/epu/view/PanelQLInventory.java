    public void fillDataCbboxKhoHang()
    {
        DefaultComboBoxModel cbboxModel = (DefaultComboBoxModel) cbboxKhoHang.getModel();   
        for(var w : new WareHouseController().getAll())
            cbboxModel.addElement(w.getWareHouseName());
    }
    public void fillDataTableSPTrongKho(String choosenWareHouseName)
    {
        int WareHouseId = getWareHouseIdIsChoosen(choosenWareHouseName);
        
        List<Inventory> listInventoryByWareHouseId = new InventoryController().getByWareHouseId(WareHouseId);
        DefaultTableModel tableModel = (DefaultTableModel) tableSPTonKho.getModel();
        String[] columnName = {"InventoryId","ProductId","WareHouseId","ShelfId","Quantity"};
        tableModel.setDataVector(new Object[][]{}, columnName);
        
        for(var i : listInventoryByWareHouseId)
        {
            Object[] rowData = {i.getInventyId(),i.getProductId(),i.getWareHouseId(),i.getShelfId(),i.getQuantity()};
            tableModel.addRow(rowData);
            
        }   
    } 
    public int getWareHouseIdIsChoosen(String wareHouseName)
    {
        int WareHouseId = 0;
        List<WareHouse> whs = new WareHouseController().getAll();
        for(var w : whs)
        {
            if(w.getWareHouseName().equals(wareHouseName))
                WareHouseId = w.getWareHouseId();
        } 
        return WareHouseId;
    }
    
