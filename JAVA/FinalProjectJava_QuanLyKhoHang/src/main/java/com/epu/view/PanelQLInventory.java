    private void btnExportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportActionPerformed
        // Tạo ra fileChooser để lưu file
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Chọn nơi lưu file Excel");
        fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        fileChooser.setAcceptAllFileFilterUsed(false);
        fileChooser.addChoosableFileFilter(new FileNameExtensionFilter("Excel Files","xlsx" ,"xls") );
        int userSelect = fileChooser.showSaveDialog(this);
        
        if(userSelect == JFileChooser.APPROVE_OPTION)
        {
            File fileSave = fileChooser.getSelectedFile();
            String filePath = fileSave.getAbsolutePath();
            if(!filePath.endsWith(".xlsx"))
                filePath += ".xlsx";
            
            new ExcelInventory().exportToExcel(filePath, tableSPTonKho, "Inventory of "+String.valueOf(cbboxKhoHang.getSelectedItem()));
            JOptionPane.showMessageDialog(this, "Xuất data thành công");  
        }   
    }//GEN-LAST:event_btnExportActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // Insert
        if(checkEdit == false)
        {
            int quantity = Integer.parseInt(txtQuantity.getText());
            int WareHouseId = Integer.parseInt(txtWareHouseId.getText());
            String choosenPName = String.valueOf(cbboxProduct.getSelectedItem());
            int productId = 0;
            List<Product> list = new ProductController().getAll();
            for(var p : list)
                if(p.getProductName().equals(choosenPName))
                {
                    productId = p.getProductId();
                    break;
                }
            int shelfId = (int) (cbboxShelf.getSelectedItem());
            Inventory i = new Inventory(productId, WareHouseId, shelfId, quantity);
            if(new InventoryController().Insert(i))
            {
                JOptionPane.showMessageDialog(this, "Insert Success!");
                txtQuantity.setText("");
                
                cbboxProduct.setSelectedIndex(0);
                cbboxShelf.setSelectedIndex(0);
                fillDataTableSPTrongKho(String.valueOf(cbboxKhoHang.getSelectedItem()));
            }
            else 
                JOptionPane.showMessageDialog(this, "Insert Fail!");
            
            
        } // Update
        else {
            int quantity = Integer.parseInt(txtQuantity.getText());
            int WareHouseId = Integer.parseInt(txtWareHouseId.getText());
            String choosenPName = String.valueOf(cbboxProduct.getSelectedItem());
            int productId = 0;
            List<Product> list = new ProductController().getAll();
            for(var p : list)
                if(p.getProductName().equals(choosenPName))
                {
                    productId = p.getProductId();
                    break;
                }
            int shelfId = (int) (cbboxShelf.getSelectedItem());
            Inventory i = new Inventory(choosenInven.getInventyId(), productId, WareHouseId, shelfId, quantity);
            if(new InventoryController().Update(i))
            {
                JOptionPane.showMessageDialog(this, "Update Success!");
                txtQuantity.setText("");
                cbboxProduct.setSelectedIndex(0);
                cbboxShelf.setSelectedIndex(0);
                fillDataTableSPTrongKho(String.valueOf(cbboxKhoHang.getSelectedItem()));
            }
            else 
                JOptionPane.showMessageDialog(this, "Update Fail!");
        }
        
        
    }//GEN-LAST:event_btnSaveActionPerformed
