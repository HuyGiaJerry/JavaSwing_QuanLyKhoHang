    private void btnPageUpdateTonKhoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPageUpdateTonKhoActionPerformed

        if(this.tableSPTonKho.getSelectedRow() < 0)
            JOptionPane.showMessageDialog(this, "Hãy chọn Iventory cần sửa !");
        else 
        {
            
            CardLayout cl = (CardLayout) mainLayout.getLayout();
            cl.show(mainLayout, "editPanel");
            checkEdit = true;
            lbEdit.setText("SỬA ĐỔI TỒN KHO");
            txtQuantity.setText(String.valueOf(choosenInven.getQuantity()));
            txtWareHouseId.setText(String.valueOf(choosenInven.getWareHouseId()));
            List<Product> prs = new ProductController().getAll();
            String proName = "";
            for(var p : prs)
                if(p.getProductId() == choosenInven.getProductId())
                {
                    proName = p.getProductName();
                    break;
                }
            
            cbboxProduct.setSelectedItem(proName);
            
            cbboxShelf.setSelectedItem(choosenInven.getShelfId());
            
            cbboxKhoHang.setEnabled(false);
        }
    }//GEN-LAST:event_btnPageUpdateTonKhoActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if(this.tableSPTonKho.getSelectedRow() < 0)
            JOptionPane.showMessageDialog(this, "Hãy chọn Iventory cần xóa !");
        else
        {
            int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắn xóa không", "Xác nhận xóa Inventory", JOptionPane.YES_NO_OPTION);
            if(confirm == JOptionPane.YES_OPTION)
            {
                if(new InventoryController().Delete(choosenInven.getInventyId()))
                {
                    JOptionPane.showMessageDialog(this, "Delete Success!");
                    fillDataTableSPTrongKho(String.valueOf(cbboxKhoHang.getSelectedItem()));
                }
                
                else
                    JOptionPane.showMessageDialog(this, "Delete Success!");
            }
            
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

